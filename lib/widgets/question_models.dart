class Flutter {
  final String id;
  final String title;
  final Map<String, bool> option;
  Flutter({
    required this.id,
    required this.title,
    required this.option,
  });
  @override
  String toString() {
    return 'Flutter(id:$id,title:$title,option:$option)';
  }
}

